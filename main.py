def add(x,y):
    return x+y
def subtract(x,y):
    return x-y
def multiply(x,y):
    return x*y
def divide(x,y):
    return x/y
numb1=int(input("enter first number:"))
numb2=int(input("enter second number:"))
print("the sum is:", add(numb1,numb2))
print("the difference is:", subtract(numb1,numb2))
print("the product is:", multiply(numb1,numb2))
print("the quotient is:", divide(numb1,numb2))